class Veiculos
{
    public string cor {get;set;}
    public string modelo {get;set;}
    public string placa {get;set;}
    public string ano {get;set;}
    
    public Veiculos(string cor, string modelo, string placa, string ano)
    {
        this.cor = cor;
        this.modelo = modelo;
        this.placa = placa;
        this.ano = ano;

    }
    
}